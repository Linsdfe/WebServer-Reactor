#!/bin/bash

# ==========================================
# WebServer 压测脚本 (自动清空系统缓存)
# ==========================================

echo "=========================================="
echo "  开始压测准备..."
echo "=========================================="

# 1. 同步磁盘数据 (防止数据丢失)
echo "[1/4] 正在同步磁盘数据 (sync)..."
sync

# 2. 清空系统缓存 (需要 root 权限)
# 3 = 清空页缓存 + 目录项 + inodes
echo "[2/4] 正在清空系统缓存 (drop_caches)..."
sudo sh -c 'echo 3 > /proc/sys/vm/drop_caches'

if [ $? -eq 0 ]; then
    echo "        系统缓存已清空。"
else
    echo "        警告: 缓存清空失败，请检查是否有 sudo 权限。"
fi

# 3. 等待一小会儿，让系统稳定
echo "[3/4] 系统稳定中..."
sleep 2

# 4. 执行压测
echo "[4/4] 开始 wrk 压测..."
echo "=========================================="
echo ""

# 这里是你的 wrk 命令
wrk -t12 -c200 -d30s http://127.0.0.1:8888/index.html

echo ""
echo "=========================================="
echo "  压测完成！"
echo "=========================================="
